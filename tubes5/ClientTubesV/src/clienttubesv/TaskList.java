    public void setData(String s) {
        String[] pecah = s.split("\n");
        banyak = Integer.parseInt(pecah[0]);
        id_tugas = new int[banyak];
        nama_tugas = new String[banyak];
        deadline = new String[banyak];
        status = new String[banyak];
        tag = new String[banyak];
        assignee = new String[banyak];
        kategori = new String[banyak];
        
        int indeks = 0;
        for (int i=0;i<banyak;++i) {
            id_tugas[i] = Integer.parseInt(pecah[++indeks]);
            nama_tugas[i] = pecah[++indeks];
            kategori[i] = pecah[++indeks];
            if (pecah[++indeks].equals("1")) status[i] = "DONE";
            else status[i] = "NOT DONE YET";
            deadline[i] = pecah[++indeks];
            tag[i] = pecah[++indeks];
            StringBuilder sb = new StringBuilder();
            int temp = Integer.parseInt(pecah[++indeks]);
            for (int j=0;j<temp;++j) {
                sb.append(pecah[++indeks]).append("\n");
            }
            assignee[i] = sb.toString();
        }
        
        jList1.setModel(new javax.swing.AbstractListModel() {
            String[] strings = nama_tugas;
            @Override
            public int getSize() { return strings.length; }
            @Override
            public Object getElementAt(int i) { return strings[i]; }
        });
    }
